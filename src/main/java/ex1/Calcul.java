package ex1;

public class Calcul {

    /**
     * Méthode pour soustraire deux entiers
     *
     * @param a, le premier nombre
     * @param b, le second nombre
     * @return la différence de a et b
     */
    public int add(int a, int b) {
        return a + b;
    }

    /**
     * Méthode pour soustraire deux entiers
     *
     * @param a, le premier nombre
     * @param b, le second nombre
     * @return la somme de a et b
     */

    public int sub(int a, int b) {
        return a - b;
    }
}